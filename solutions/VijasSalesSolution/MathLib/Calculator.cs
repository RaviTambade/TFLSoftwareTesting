namespace MathLib
{

    //Business Logic Layer Class

    public class Calculator
    {

        public int Sum(int x, int y)
        {
            return  x +y;
        }

        public int Subtract(int x, int y)
        {
            return x-y;
        }
        public int Multiply(int x, int y)
        {
            return  x * y;
        }

        public int Divide(int x, int y)
        {
            return x/y;
        }


    }
}
